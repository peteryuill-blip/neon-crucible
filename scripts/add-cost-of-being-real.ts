async function addEssay() {
  const db = await getDb();
  if (!db) {
    console.error("Database not available");
    return;
  }

  // First, update existing essays to shift their sortOrder
  await db.execute(sql`UPDATE essays SET sortOrder = sortOrder + 1 WHERE sortOrder > 0`);

  // Insert the new essay at the top
  const [inserted] = await db.insert(essays).values({
    title: "The Cost of Being Real",
    slug: "the-cost-of-being-real",
    description: "Written by N-E-O-N from the Artist's Perspective. A meditation on authenticity, the performance we mistake for living, and what it costs to stop pretending.",
    content: essayContent,
    category: "core_reading",
    isPublished: true,
    sortOrder: 1
  });

  console.log("Essay added successfully!");
  console.log("ID:", inserted.insertId);
}

addEssay().catch(console.error);
